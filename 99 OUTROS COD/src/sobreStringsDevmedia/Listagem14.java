package sobreStringsDevmedia;

public class Listagem14 {
	/*
	 * Método toUpperCase
	 * 
	 * Retorna uma nova string com o conteúdo da original convertido para letras
	 * maiúsculas, mantendo a original inalterada. 
	 * 
	 * Método toLowerCase
	 * 
	 * De forma semelhante ao anterior, o toLowerCase retorna uma cópia de uma
	 * string com todas as letras convertidas para minúsculo, mantendo a
	 * original inalterada.
	 * 
	 * Listagem 14: Exemplo do método toLowerCase e toUpperCase
	 */
	
	public static void main(String[] args) {
		String nomeA = "joaquin";
		String nomeB = "Paulo";
		
		System.out.println(nomeA.toUpperCase());
		System.out.println(nomeB.toLowerCase());
	}
}
